package service;

import entidades.Customer;
import entidades.Paids;
import java.util.List;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;
import javax.ws.rs.Consumes;
import javax.ws.rs.DELETE;
import javax.ws.rs.GET;
import javax.ws.rs.POST;
import javax.ws.rs.PUT;
import javax.ws.rs.Path;
import javax.ws.rs.PathParam;
import javax.ws.rs.Produces;

/**
 *
 * @author Saul
 */
@Stateless
@Path("paids")
public class PaidsFacadeREST extends AbstractFacade<Paids> {
    @PersistenceContext(unitName = "com.mycompany_StoreBackend_war_1.0-SNAPSHOTPU")
    private EntityManager em;

    public PaidsFacadeREST() {
        super(Paids.class);
    }

    @POST
    @Override
    @Consumes({"application/xml", "application/json"})
    public void create(Paids entity) {
        Paids paid = entity;
        Customer customer = paid.getCustomerId();
        customer.setSaldo(paid.getAmount()+customer.getSaldo());
        em.merge(customer);
        super.create(entity);
    }

    @PUT
    @Path("{id}")
    @Consumes({"application/xml", "application/json"})
    public void edit(@PathParam("id") Integer id, Paids entity) {
        super.edit(entity);
    }

    @DELETE
    @Path("{id}")
    public void remove(@PathParam("id") Integer id) {
        super.remove(super.find(id));
    }

    @GET
    @Path("{id}")
    @Produces({"application/xml", "application/json"})
    public Paids find(@PathParam("id") Integer id) {
        return super.find(id);
    }

    @GET
    @Override
    @Produces({"application/xml", "application/json"})
    public List<Paids> findAll() {
        return super.findAll();
    }

    @GET
    @Path("{from}/{to}")
    @Produces({"application/xml", "application/json"})
    public List<Paids> findRange(@PathParam("from") Integer from, @PathParam("to") Integer to) {
        return super.findRange(new int[]{from, to});
    }

    @GET
    @Path("count")
    @Produces("text/plain")
    public String countREST() {
        return String.valueOf(super.count());
    }

    @Override
    protected EntityManager getEntityManager() {
        return em;
    }
    
}
